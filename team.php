<div id="content-body"><p>With over 25 years of executive experience working with high-tech companies and venture capital firms, Zur Feldman has helped transform numerous startups into multi-billion dollar corporations.<br />
   <br />
  Prior to joining wefi, Zur was a venture partner at Pitango – Israel’s largest VC firm.  While there, he spearheaded many successful investment initiatives, both within the public and private spheres.<br />
   <br />
  Before Pitango, Zur was selected by Microsoft, ING, Echostar, and Gilat to cofound and lead StarBand Communications – the world’s first 2-way, high-speed consumer satellite broadband service.  During his 5 years as Chairman and CEO, he expanded Internet coverage to tens of thousands of rural customers throughout North and Central America.<br />
   <br />
  Prior to StarBand, Zur served in a range of senior positions at Packard Bell Electronics over the course of 13 years.  As the company’s Executive VP of Operations, he oversaw the successful migration of Packard Bell’s HQ operations following the Northbridge earthquake of 1995.  This massive transition resulted in the formation of the world’s largest PC manufacturing facility under a single roof.  While at Packard Bell, Zur also spearheaded several M&amp;A initiatives with Zenith Corp. and NEC USA Corp.<br />
   <br />
  In addition to a BA in Photojournalism, TV Broadcasting, and Communications from NYU, Zur is a graduate of the Harvard Business School program for Senior Executives, the Stanford University Graduate School of Business and Management for Executives, and UCLA’s Business and Management program.</p>
